package Entidades;

/**
 *
 * @author Wallace
 */
public class Coordenadores {
    private int cpfcoord;
    private String nomecord;
    private String titulacao;
    private int dedicacao;

    public int getCpfcoord() {
        return cpfcoord;
    }

    public void setCpfcoord(int cpfcoord) {
        this.cpfcoord = cpfcoord;
    }

    public String getNomecord() {
        return nomecord;
    }

    public void setNomecord(String nomecord) {
        this.nomecord = nomecord;
    }

    public String getTitulacao() {
        return titulacao;
    }

    public void setTitulacao(String titulacao) {
        this.titulacao = titulacao;
    }

    public int getDedicacao() {
        return dedicacao;
    }

    public void setDedicacao(int dedicacao) {
        this.dedicacao = dedicacao;
    }
        
}
